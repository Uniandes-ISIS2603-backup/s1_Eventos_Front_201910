

/**
* This class represents a Agenda of the AgendaStore. 
* It contains all the information relevant to the Agenda.
*/
export class Agenda {
    /**
    * The Agenda's id
    */
    id: number;

    /**
    * The Agenda's name
    */
    name: string;


    /**
    * The activity of the Agenda
    */
    actividad: string;

    /**
     * The Agenda's end date
     */
    horaFinal: any;
    
    /**
     * The Agenda's start date
     */
    horaInicio: any;

}
