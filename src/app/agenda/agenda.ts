

/**
* This class represents a Agenda of the AgendaStore. 
* It contains all the information relevant to the Agenda.
*/
export class Agenda {
    /**
    * The Agenda's id
    */
    id: number;

    /**
    * The Agenda's name
    */
    name: string;

    /**
    * The Agenda's ISBN
    */
    isbn: string;

    /**
    * A brief summary of the Agenda
    */
    description: string;

    /**
    * The location of the Agenda's image
    */
    image: string;

    /**
     * The Agenda's publishing date
     */
    publishingdate: any;
    

}
