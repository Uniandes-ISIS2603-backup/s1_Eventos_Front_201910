/**
* This class represents a Multimedia. 
* It contains all the information relevant to the multimedia.
*/
export interface Multimedia {
    /**
    * The multimedia
    */
    id: number;

    /**
    * The url
    */
    url: string;

    /**
    * The nombre
    */
    nombre: string;

    /**
    * Tipo
    */
    tipo: string;

    /**
    * The Memoria
    */
    memoria: boolean;

}
